
# Functies checklist

# 1. A function begins with def
# 2. Does your function name have only characters i
#    and _ (underscore) characters?
# 3. achter de functienaam haakje openen.
# 4. tussen de haakjes kun je argument plaatsen gescheiden
#    met komma's.
# 5. Elk argument moet uniek zijn.
# 6. Een haakje sluiten achter het laatste argument.
# 7. goede indent, inspring. (met vim gaat die automatisch)
# 8. Eindigen van functie met het beeindigen van de inspring.

# Wanneer je een functie uitvoerd:
# Heb je de goede naam van de functie getypt.
# Haakje openen neerzetten achter de functienaam
# De argument values moeten gescheiden worden door komma's
# Eindigen van uitvoeren functie met haakje sluiten

